var express = require('express');
var passport = require('passport');
var Strategy = require('passport-saml').Strategy;


passport.use('saml2', new Strategy({
    path: '/login/callback',
    entryPoint: 'http://localhost:8080/realms/myrealm/protocol/saml',
    issuer: 'example'
  },
  function(profile, cb) {
    console.log("profile", profile)
    return cb(null, profile);
  }));


passport.serializeUser(function(user, done) {
  done(null, user);
});

passport.deserializeUser(function(obj, done) {
  done(null, obj);
});


// Create a new Express application.
var app = express();

// Configure view engine to render EJS templates.
app.set('views', __dirname + '/views');
app.set('view engine', 'ejs');

// Use application-level middleware for common functionality, including
// logging, parsing, and session handling.
app.use(require('morgan')('combined'));
app.use(require('cookie-parser')());
app.use(require('body-parser').urlencoded({ extended: true }));
app.use(require('express-session')({ secret: 'keyboard cat', resave: true, saveUninitialized: true }));

// Initialize Passport and restore authentication state, if any, from the
// session.
app.use(passport.initialize());
app.use(passport.session());


// Define routes.
app.get('/',
  function(req, res) {
    console.log(req)
    res.render('home', { user: req.user });
  });

app.get('/login',
  function(req, res){
    console.log(req)
    res.render('login');
  });

app.get('/login/idp',
  passport.authenticate('saml2'));

app.post('/login/callback',
  passport.authenticate('saml2', { failureRedirect: '/login' }),
  function(req, res) {
    console.log(req)
    res.redirect('/');
  });

app.get('/profile',
  require('connect-ensure-login').ensureLoggedIn(),
  function(req, res){
    res.render('profile', { user: req.user });
  });

app.listen(3000);
